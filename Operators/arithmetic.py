# floor division - basically discards the remainder by rounding down to the closest whole number or integer, which is less than or equal to the normal division result. 
print(10 / 4)       # yields 2.5
print(10 // 4)      # yields 2 
print(10 / 7)       # yields ~ 1.4
print(10 // 7)      # yields 1

