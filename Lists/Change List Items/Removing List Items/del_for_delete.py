# the 'del' keyword can also remove an item at the specified index: 
mylist = ["apple", "banana", "cherry"]
del mylist[0]
print(mylist)

# the del keyword can also delete the entire list completely
thislist = ["kiwi", "pineapple", "papaya"]
del thislist
print(thislist)   # will throw an error - 'thislist' is not defined