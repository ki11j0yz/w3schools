# loop through list items by using 'for' loop:
mylist = ["apple", "banana", "cherry"]
for x in mylist:
    print(x)


### Notes on 'for' loops
    # used for iterating over a sequence (that is either a list, tuple, a dictionary, a set, or a string)
    # with a 'for' loop we can execute a set of statements, once for eatch item in a list, tuple, set, dictionary, etc. 
    
# looping through a string:
for x in "banana":
    print(x)