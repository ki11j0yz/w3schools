# sort() orders alphanumerically, ascending by default:

thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort()         # orders alphanumerically
print(thislist)

# numerically:
numList = [100, 45, 3, 23, 78]
print(numList)
numList.sort()
print(numList)

# sort descending:
nameList = ["James", "Jourdan", "Monica", "Jerusha", "Mateo", "Elvis", "Zarina"]
print(nameList)
nameList.sort(reverse = True)
print(nameList)

lastList = ["Jerry", "Matt", "Karl", "Heinrich", "McDougal", "Farnesworth"]
print(lastList)
lastList.sort()
print(lastList)
