# NOTE: Packing a tuple is creating it, and assigning values to it, for example: 
# cars = ("Honda", "Toyota", "Saab")
# NOTE: Unpacking a tuple extracts the values back into variables, for example: 
cars = ("Honda", "Toyota", "Saab")

# Here you are assigning values to the elements of the tuple, or unpacking the damn thing as they say
(obj1, obj2, obj3) = cars

print(obj1)
print(obj2)
print(obj3)

