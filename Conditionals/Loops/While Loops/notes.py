# NOTE: Python has two primitive loop commands: 'while' and 'for' loops - In this directory, I will focus on 'while' loops.
# NOTE: A 'while' loop is one where we can execute a set of statements as long as a condition is true  - for example, print 'i' as long as i is < 9:
i = 1
while i < 9:
    print(i)
    i += 1


# NOTE: notice we est. a value for 'i', then while 'i' is less than the integer 9, we print 'i' and then we add a counter at the end of the loop in order to progress what 'i' is.
    

 