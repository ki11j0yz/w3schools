# NOTE: 'for' loops are used for iterating over a sequence (be it either a list, tuple, dictionary, set or a string).
# NOTE: syntax: for <variable> in <sequence>: <yielded statements>

# looping through a list:
languages = ["Python", "Perl", "C++", "C#", "Ruby"]
for lang in languages:
    print(lang)
    
# looping through a string:
for x in "onomatopoeia":
    print(x)
    
